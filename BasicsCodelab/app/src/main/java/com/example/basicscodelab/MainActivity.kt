// 앱의 진입점입니다. setContent 함수를 사용하여 Composable 함수인 MyApp을 액티비티의 콘텐츠로 설정합니다.
class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            BasicsCodelabTheme {
                MyApp(modifier = Modifier.fillMaxSize())
            }
        }
    }
}

// 앱의 전체 레이아웃을 정의합니다. Surface 컴포넌트를 사용하여 배경색을 설정하고, shouldShowOnboarding 변수에 따라 Onboarding 화면이나 환영 메시지가 표시됩니다.
@Composable
fun MyApp(modifier: Modifier = Modifier) {
    var shouldShowOnboarding by rememberSaveable { mutableStateOf(true) }

    Surface(modifier, color = MaterialTheme.colorScheme.background) {
        if (shouldShowOnboarding) {
            OnboardingScreen(onContinueClicked = { shouldShowOnboarding = false })
        } else {
            Greetings()
        }
    }
}

// 앱에 처음 진입했을 때 사용자에게 보여지는 화면을 정의합니다.
@Composable
fun OnboardingScreen(
    onContinueClicked: () -> Unit,
    modifier: Modifier = Modifier
) {
    Column(
        modifier = modifier.fillMaxSize(),
        verticalArrangement = Arrangement.Center,
        horizontalAlignment = Alignment.CenterHorizontally
    ) {
// 환영 메시지와 계속하기 버튼이 표시됩니다. 버튼을 클릭하면 onContinueClicked 콜백이 호출되어 Onboarding 화면을 숨깁니다.
        Text("Welcome to the Basics Codelab!")
        Button(
            modifier = Modifier.padding(vertical = 24.dp),
            onClick = onContinueClicked
        ) {
            Text("Continue")
        }
    }
}

// 사용자에게 인사하는 화면을 정의합니다. LazyColumn을 사용하여 많은 양의 데이터를 효율적으로 표시합니다.
// 각 항목에 대한 데이터는 names 리스트에 저장되어 있습니다.
@Composable
private fun Greetings(
    modifier: Modifier = Modifier,
    names: List<String> = List(1000) { "$it" }
) {
    LazyColumn(modifier = modifier.padding(vertical = 4.dp)) {
        items(items = names) { name ->
            Greeting(name = name)
        }
    }
}

// 개별 인사 메시지를 정의합니다. 각 항목은 Card 컴포넌트로 래핑되어 있으며, 확장 가능한 섹션을 가집니다.
// 사용자가 아이콘 버튼을 클릭하면 섹션이 펼쳐지거나 축소됩니다.
@Composable
private fun Greeting(name: String, modifier: Modifier = Modifier) {
    Card(
        colors = CardDefaults.cardColors(
            containerColor = MaterialTheme.colorScheme.primary
        ),
        modifier = modifier.padding(vertical = 4.dp, horizontal = 8.dp)
    ) {
        CardContent(name)
    }
}

// 카드에 표시되는 내용을 정의합니다. 이 내용에는 사용자의 이름과 함께 표시되는 텍스트가 포함되며,
@Composable
private fun CardContent(name: String) {
    var expanded by rememberSaveable { mutableStateOf(false) }

    Row(
        modifier = Modifier
            .padding(12.dp)
            .animateContentSize(
                animationSpec = spring(
                    dampingRatio = Spring.DampingRatioMediumBouncy,
                    stiffness = Spring.StiffnessLow
                )
            )
    ) {
        Column(
            modifier = Modifier
                .weight(1f)
                .padding(12.dp)
        ) {
            Text(text = "Hello, ")
            Text(
                text = name, style = MaterialTheme.typography.headlineMedium.copy(
                    fontWeight = FontWeight.ExtraBold
                )
            )
            if (expanded) {
                Text(
                    text = ("Composem ipsum color sit lazy, " +
                            "padding theme elit, sed do bouncy. ").repeat(4),
                )
            }
        }
// 사용자가 섹션을 확장하거나 축소할 수 있는 아이콘 버튼도 포함됩니다.
        IconButton(onClick = { expanded = !expanded }) {
            Icon(
                imageVector = if (expanded) Filled.ExpandLess else Filled.ExpandMore,
                contentDescription = if (expanded) {
                    stringResource(R.string.show_less)
                } else {
                    stringResource(R.string.show_more)
                }
            )
        }
    }
}

